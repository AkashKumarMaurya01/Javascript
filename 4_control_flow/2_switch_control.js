// Switch case 
const month = 'April'
switch (month) {
    case 'Jan':
        console.log("Janauary");
        break;
    case 'Feb':
        console.log("Febuary");
        break;
    case 'March':
        console.log("March");
        break;
    case 'April':
        console.log("April");
        break;

    default:
        console.log("the default case");
        break;
}